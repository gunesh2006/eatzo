export const placeOrder = async (req, res) => {
  console.log("placeOrder API HIT");
  try {
    const { cartItems, paymentMethod, deliveryAddress, totalAmount } = req.body;

    if (!cartItems || cartItems.length == 0) {
      return res.status(400).json({ message: "cart is empty" });
    }

    if (
      !deliveryAddress.text ||
      !deliveryAddress.latitude ||
      !deliveryAddress.longitude
    ) {
      return res
        .status(400)
        .json({ message: "send complete delivery address" });
    }

    const groupItemsByShop = {};

    cartItems.forEach((item) => {
      const shopId = item.shop;
      if (!groupItemsByShop[shopId]) {
        groupItemsByShop[shopId] = [];
      }
      groupItemsByShop[shopId].push(item);
    });

    const shopOrders = await Promise.all(
      Object.keys(groupItemsByShop).map(async (shopId) => {
        const shop = await Shop.findById(shopId).populate("owner");
        if (!shop) {
          throw new Error("shop not found");
        }
        const items = groupItemsByShop[shopId];
        console.log("ITEMS:", items);
        const subtotal = items.reduce(
          (sum, i) => sum + Number(i.price) * Number(i.quantity),
          0,
        );

        return {
          shop: shop._id,
          owner: shop.owner._id,
          subtotal,
          shopOrderItems: items.map((i) => ({
            item: i.id,
            price: i.price,
            quantity: i.quantity,
            name: i.name,
          })),
        };
      }),
    );

    //payment gateway

    const INRtoUSD = (inrAmount) => {
      const conversionRate = 0.012; // 1 INR â‰ˆ 0.012 USD (example)
      return (inrAmount * conversionRate).toFixed(2);
    };

    const amountInUSD = INRtoUSD(totalAmount);

    if (paymentMethod === "online") {
      const paypalOrder = await createPaypalOrder(amountInUSD);

      const draftOrder = await Order.create({
        user: req.userId,
        paymentMethod: "online",
        paymentStatus: "pending",
        deliveryAddress,
        totalAmount: totalAmount,
        shopOrders,
        paypalOrderId: paypalOrder.id,
      });

      return res.status(200).json({
        message: "PayPal order created",
        paypalOrderId: paypalOrder.id,
        orderId: draftOrder._id,
      });
    }

    const newOrder = await Order.create({
      user: req.userId,
      paymentMethod,
      deliveryAddress,
      totalAmount,
      shopOrders,
    });

    await newOrder.populate(
      "shopOrders.shopOrderItems.item",
      "name image price",
    );
    await newOrder.populate("shopOrders.shop", "name");
    await newOrder.populate("shopOrders.owner", "fullName socketId");
    await newOrder.populate("user", "fullName email mobile");

    const io = req.app.get("io");
    if (io) {
      newOrder.shopOrders.forEach((shopOrder) => {
        const ownerSocketId = shopOrder.owner.socketId;
        if (ownerSocketId) {
          io.to(ownerSocketId).emit("newOrder", {
            _id: newOrder._id,
            paymentMethod: newOrder.paymentMethod,
            user: newOrder.user,
            shopOrders: shopOrder,
            createdAt: newOrder.createdAt,
            deliveryAddress: newOrder.deliveryAddress,
          });
        }
      });
    }

    sendInvoiceMail(newOrder.user, newOrder).catch((err) =>
      console.error("Invoice email failed:", err),
    );
    return res.status(201).json(newOrder);
  } catch (error) {
    return res.status(500).json({ message: `place order error:${error}` });
  }
};

